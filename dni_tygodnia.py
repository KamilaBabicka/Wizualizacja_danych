def dni_numer(x):
    if x == 1:
        print("Poniedzialek")
    elif x == 2:
        print("Wtorek")
    elif x == 3:
        print("Środa")
    elif x == 4:
        print("Czwartek")
    elif x == 5:
        print("Piątek")
    elif x == 6:
        print("Sobota")
    elif x == 7:
        print("Niedziela")
    else:
        print("Błędny numer")

